// InvoiceProcessingWorkflow demonstrates comprehensive invoice management
func (demo *TCOLBusinessExamples) InvoiceProcessingWorkflow() BusinessScenario {
	scenario := BusinessScenario{
		Name:        "Invoice Processing Workflow",
		Description: "End-to-end invoice lifecycle from creation to payment including approval, sending, reminders, and collections",
		Commands: []string{
			"// === INVOICE CREATION ===",
			"// Create invoice from order",
			"ORDER.APPROVE order_id=\"ORD-2024-156\" approver=\"manager.finance\"",
			"INVOICE.CREATE order_id=\"ORD-2024-156\" type=\"standard\" currency=\"EUR\"",
			"",
			"// Add invoice details",
			"INVOICE:last_created:invoice_number=\"INV-2024-001578\" :invoice_date=\"2024-07-26\"",
			"INVOICE:last_created:due_date=\"2024-08-25\" :payment_terms=\"NET30\"",
			"INVOICE:last_created:tax_rate=0.19 :discount_percent=5.0",
			"",
			"// Add line items",
			"INVOICE.ADD-ITEM invoice_id=last_created product=\"SRV-CONSULTING\" quantity=40 unit_price=150.00",
			"INVOICE.ADD-ITEM invoice_id=last_created product=\"SRV-SUPPORT\" quantity=1 unit_price=2400.00",
			"INVOICE.ADD-ITEM invoice_id=last_created product=\"LIC-SOFTWARE\" quantity=10 unit_price=299.00",
			"",
			"// === APPROVAL WORKFLOW ===",
			"// Route for approval based on amount",
			"IF INVOICE:last_created:total_amount>10000 THEN {",
			"    APPROVAL.REQUEST invoice_id=last_created approver=\"director.finance\" level=\"high\"",
			"} ELSE {",
			"    APPROVAL.REQUEST invoice_id=last_created approver=\"manager.finance\" level=\"standard\"",
			"}",
			"",
			"// Approval process",
			"APPROVAL.REVIEW invoice_id=last_created reviewer=\"manager.finance\" status=\"approved\"",
			"INVOICE:last_created:approval_date=\"2024-07-26\" :approved_by=\"manager.finance\"",
			"",
			"// === INVOICE DISPATCH ===",
			"// Finalize and send invoice",
			"INVOICE.FINALIZE invoice_id=last_created",
			"INVOICE.SEND invoice_id=last_created method=\"email\" copy_accounting=true",
			"",
			"// Track delivery",
			"INVOICE:last_created:sent_date=\"2024-07-26\" :delivery_status=\"delivered\"",
			"CUSTOMER.NOTIFY customer_id=invoice.customer_id message=\"Invoice sent\" method=\"email\"",
			"",
			"// === PAYMENT TRACKING ===",
			"// Monitor payment status",
			"INVOICE[due_date<=\"2024-08-25\",status=\"sent\"].LIST | REPORT.GENERATE type=\"payment_due\"",
			"",
			"// Automated reminders",
			"SCHEDULE.CREATE trigger=\"due_date-7\" command=\"INVOICE.SEND-REMINDER type='friendly'\"",
			"SCHEDULE.CREATE trigger=\"due_date+1\" command=\"INVOICE.SEND-REMINDER type='overdue'\"",
			"SCHEDULE.CREATE trigger=\"due_date+30\" command=\"INVOICE.SEND-REMINDER type='final_notice'\"",
			"",
			"// === PAYMENT PROCESSING ===",
			"// Record payment",
			"PAYMENT.CREATE invoice_id=last_created amount=9282.00 method=\"bank_transfer\"",
			"PAYMENT:last_created:payment_date=\"2024-08-20\" :reference=\"TXN-789456123\"",
			"INVOICE:last_created:status=\"paid\" :paid_date=\"2024-08-20\" :paid_amount=9282.00",
			"",
			"// Handle partial payments",
			"IF PAYMENT:last_created:amount < INVOICE:last_created:total_amount THEN {",
			"    INVOICE:last_created:status=\"partially_paid\"",
			"    TASK.CREATE title=\"Follow up partial payment\" assignee=\"accounting.team\"",
			"}",
			"",
			"// === COLLECTIONS PROCESS ===",
			"// Overdue invoice management",
			"INVOICE[status=\"overdue\",days_overdue>30].LIST | COLLECTIONS.ASSIGN priority=\"medium\"",
			"INVOICE[status=\"overdue\",days_overdue>60].LIST | COLLECTIONS.ASSIGN priority=\"high\"",
			"INVOICE[status=\"overdue\",days_overdue>90].LIST | COLLECTIONS.ASSIGN priority=\"critical\"",
			"",
			"// Collections actions",
			"COLLECTIONS.CALL customer_id=invoice.customer_id outcome=\"payment_promised\" date=\"2024-08-30\"",
			"COLLECTIONS.EMAIL customer_id=invoice.customer_id template=\"formal_demand\" cc=\"legal.team\"",
			"",
			"// === REPORTING AND ANALYTICS ===",
			"// Generate financial reports",
			"REPORT.GENERATE type=\"aging_report\" period=\"2024-07\" format=\"excel\"",
			"REPORT.GENERATE type=\"revenue_recognition\" period=\"2024-Q3\" basis=\"accrual\"",
			"ANALYTICS.CALCULATE metric=\"days_sales_outstanding\" period=\"rolling_3_months\"",
		},
		Context: map[string]interface{}{
			"process_type":    "Financial Operations",
			"automation_level": "High",
			"compliance":      "SOX, GDPR",
			"integration":     "ERP, Banking, CRM",
		},
	}
	
	demo.scenarios = append(demo.scenarios, scenario)
	demo.logScenario(scenario)
	return scenario
}

