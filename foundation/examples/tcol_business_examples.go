// logScenario prints a formatted business scenario
func (demo *TCOLBusinessExamples) logScenario(scenario BusinessScenario) {
	fmt.Printf("\n" + strings.Repeat("=", 80) + "\n")
	fmt.Printf("BUSINESS SCENARIO: %s\n", scenario.Name)
	fmt.Printf("%s\n", strings.Repeat("=", 80))
	fmt.Printf("Description: %s\n\n", scenario.Description)
	
	// Print context information
	if len(scenario.Context) > 0 {
		fmt.Println("Context:")
		for key, value := range scenario.Context {
			fmt.Printf("  â€¢ %s: %v\n", key, value)
		}
		fmt.Println()
	}
	
	fmt.Println("TCOL Commands:")
	fmt.Println(strings.Repeat("-", 40))
	
	for i, cmd := range scenario.Commands {
		if strings.HasPrefix(cmd, "//") || strings.HasPrefix(cmd, "#") || strings.HasPrefix(cmd, "/*") {
			fmt.Printf("\n%s\n", cmd)
		} else if strings.TrimSpace(cmd) == "" {
			fmt.Println()
		} else {
			fmt.Printf("%3d. %s\n", i+1, cmd)
		}
	}
	
	fmt.Printf("\nTotal commands in scenario: %d\n", len(scenario.Commands))
}

