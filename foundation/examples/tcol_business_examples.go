// BusinessScenario represents a complete business use case
type BusinessScenario struct {
	Name        string
	Description string
	Commands    []string
	Context     map[string]interface{}
}

// TCOLBusinessExamples demonstrates real-world business scenarios
type TCOLBusinessExamples struct {
	scenarios []BusinessScenario
}

// NewBusinessExamples creates a new business examples instance
func NewBusinessExamples() *TCOLBusinessExamples {
	return &TCOLBusinessExamples{
		scenarios: make([]BusinessScenario, 0),
	}
}

